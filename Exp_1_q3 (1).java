public class Exp_1_q3 {
    public static void main(String[] args) {
        
                // Print ASCII values of numbers 0-9
                for (int i = 0; i <= 9; i++) {
                    System.out.println("ASCII value of " + i + " is: " + (int) i);
                }
        
                // Print ASCII values of uppercase letters A-Z
                for (char c = 'A'; c <= 'Z'; c++) {
                    System.out.println("ASCII value of " + c + " is: " + (int) c);
                }
        
                // Print ASCII values of lowercase letters a-z
                for (char c = 'a'; c <= 'z'; c++) {
                    System.out.println("ASCII value of " + c + " is: " + (int) c);
                }       
            }
        }
        
    

