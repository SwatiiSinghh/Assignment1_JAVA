public class Exp_1_q2 {
    public static void main(String[] args) {
       
                int a = 5;
                int b = 10;
                
                System.out.println("Before swap: a = " + a + ", b = " + b);
                
                // Using a third variable to swap
                int temp = a;
                a = b;
                b = temp;
                
                System.out.println("After swap: a = " + a + ", b = " + b);
            }
        }
        
    

